/**
 * Pick the banner that fits the terminal width, or null when it is too narrow
 * or not a TTY (columns is undefined when output is piped) so the caller can
 * fall back to a plain text title.
 */
export function selectBanner(
  columns: number | undefined = process.stdout.columns,
): string | null {
  const cols = columns ?? 0;
  if (cols >= WIDE_COLS) return WIDE;
  if (cols >= COMPACT_COLS) return COMPACT;
  return null;
}
