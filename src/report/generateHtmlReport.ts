/**
 * Client-side filtering. STATIC — no report data is interpolated in, so
 * untrusted content can never reach a script context; it only reads the
 * (escaped) data attributes and text content the renderer produced.
 */
const SCRIPT = `
(function () {
  "use strict";
  var search = document.getElementById("f-search");
  var selects = ["f-severity", "f-category", "f-file", "f-support"].map(function (id) {
    return document.getElementById(id);
  });
  var facets = ["severity", "category", "file", "support"];
  var cards = Array.prototype.slice.call(document.querySelectorAll("[data-card]"));
  var sections = Array.prototype.slice.call(document.querySelectorAll("[data-section]"));
  var countEl = document.getElementById("f-count");
  var noMatches = document.getElementById("no-matches");
  if (!search || !countEl) return;

  function apply() {
    var q = search.value.trim().toLowerCase();
    var active = selects.map(function (s) { return s ? s.value : ""; });
    var shown = 0;
    cards.forEach(function (card) {
      var ok = true;
      for (var i = 0; i < facets.length; i++) {
        if (active[i] && card.getAttribute("data-" + facets[i]) !== active[i]) { ok = false; break; }
      }
      if (ok && q) ok = card.textContent.toLowerCase().indexOf(q) !== -1;
      card.hidden = !ok;
      if (ok) shown++;
    });
    sections.forEach(function (section) {
      var sectionCards = section.querySelectorAll("[data-card]");
      var visible = 0;
      sectionCards.forEach(function (c) { if (!c.hidden) visible++; });
      section.hidden = sectionCards.length > 0 && visible === 0;
      var count = section.querySelector("[data-count]");
      if (count) count.textContent = String(visible);
    });
    var filtering = q !== "" || active.some(function (v) { return v !== ""; });
    countEl.textContent = filtering ? "showing " + shown + " of " + cards.length : cards.length + " findings";
    if (noMatches) noMatches.hidden = !(filtering && shown === 0);
  }

  search.addEventListener("input", apply);
  selects.forEach(function (s) { if (s) s.addEventListener("change", apply); });
  apply();
})();
`;
