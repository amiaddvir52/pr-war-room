/**
 * Phase 10 — the PRIMARY user-facing report: a single self-contained HTML file
 * (`report.html`). Pure renderer (no IO), same `ReportInput` and the same
 * `reportModel.ts` derivations as the Markdown report, so the two renderings
 * can never disagree about the pool, the verdict, or what was dropped.
 *
 * Properties the file guarantees:
 *  - SELF-CONTAINED & OFFLINE: embedded CSS and a small vanilla script; no CDN,
 *    fonts, images, or network requests of any kind. The only external href is
 *    the PR's own GitHub URL (validated http(s), escaped).
 *  - SAFE AGAINST INJECTION: every untrusted string (titles, claims, evidence,
 *    file names, PR metadata, stdout/stderr) passes through `esc()`; the script
 *    block is a static string with no interpolated data and never assigns
 *    `innerHTML`.
 *  - READABLE AT SCALE: findings are cards grouped by classification, evidence
 *    shows the strongest items first with the tail collapsed, and a filter bar
 *    (severity / category / file / skeptic support / text search) works
 *    client-side on data attributes.
 *  - HONEST: skeptic/judge fallbacks (e.g. timeouts) are marked on the summary
 *    AND on each affected card — a finding kept unvalidated is never presented
 *    as validated.
 */

/** Evidence items visible before the collapsible "show more" tail. */
export const HTML_INLINE_EVIDENCE = 3;

export function renderHtmlReport(input: ReportInput): string {
  const pool = buildPool(input);
  const dropped = collectDropped(input);

  const blockers = pool.filter((f) => f.classification === "blocker");
  const shouldFix = pool.filter((f) => f.classification === "should_fix_before_review");
  const optional = pool.filter((f) => f.classification === "nice_to_have");

  const p = input.packet.pr;
  const verdict = readinessVerdict(blockers.length, shouldFix.length, input.packet.verification);
  const verdictTone =
    blockers.length > 0 ? "blocker" : shouldFix.length > 0 ? "should" : "ready";

  const skepticSummary = input.options.skepticEnabled
    ? summarizeGate(input.skepticResults)
    : null;
  const judgeSummary =
    input.options.judgeEnabled && input.ranked !== null ? summarizeGate(input.ranked) : null;

  const prHref = safeHttpHref(p.htmlUrl);
  const prLabel = esc(`${p.owner}/${p.repo}#${p.number}`);

  const body = `
<header class="topbar">
  <div class="wrap topbar-row">
    <span class="wordmark">PR&nbsp;WAR&nbsp;ROOM</span>
    <span class="topbar-meta">
      ${prHref === null ? prLabel : `<a href="${prHref}">${prLabel}</a>`}
      <span class="sep">·</span> run ${esc(input.meta.runId)}
      <span class="sep">·</span> ${esc(input.meta.generatedAt)}
    </span>
  </div>
</header>

<main class="wrap">
  <section class="hero verdict-${verdictTone}" aria-label="Review verdict">
    <p class="eyebrow">AI pre-review verdict</p>
    <h1>${esc(p.title)}</h1>
    <p class="verdict">${esc(verdict)}</p>
    ${renderFunnel(input, dropped.length)}
    ${renderTiles(blockers.length, shouldFix.length, optional.length, dropped.length)}
    ${renderGateNotes(skepticSummary, judgeSummary)}
  </section>

  ${renderFilterBar(pool)}

  <p id="no-matches" class="empty" hidden>No findings match the current filters.</p>

  ${renderSection("blockers", "Must fix before human review", "Merge-blocking problems the reviewers verified in the changed code.", blockers)}
  ${renderSection("should-fix", "Should fix before human review", "Likely human-review comments — address them to save a round-trip.", shouldFix)}
  ${renderSection("optional", "Nice to have", "Lower-stakes improvements; fix opportunistically.", optional)}

  ${renderVerification(input.packet.verification)}
  ${renderDropped(dropped)}
  ${renderArtifacts(input)}
</main>

<footer class="wrap footer">
  Generated by pr-war-room v${esc(input.meta.toolVersion)} · run ${esc(input.meta.runId)} · ${esc(input.meta.generatedAt)} · works offline, no external assets
</footer>`;

  return `<!doctype html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<meta name="color-scheme" content="light dark">
<title>${esc(`AI Pre-Review — ${p.owner}/${p.repo}#${p.number}`)}</title>
<style>${CSS}</style>
</head>
<body>
${body}
<script>${SCRIPT}</script>
</body>
</html>
`;
}

/* ------------------------------ hero pieces ------------------------------ */

function renderFunnel(input: ReportInput, droppedTotal: number): string {
  const cells = buildFunnel(input)
    .map(
      (s, i) => `
    <div class="funnel-stage" data-stage="${i}">
      <span class="funnel-value">${s.value}</span>
      <span class="funnel-label">${esc(s.label)}</span>
      <span class="funnel-hint">${esc(s.hint)}</span>
    </div>`,
    )
    .join('<div class="funnel-arrow" aria-hidden="true">→</div>');
  return `
  <div class="funnel" role="group" aria-label="Finding funnel">
    ${cells}
    <div class="funnel-dropped">${droppedTotal} dropped along the way</div>
  </div>`;
}

function renderTiles(blockers: number, shouldFix: number, optional: number, dropped: number): string {
  const tile = (cls: string, label: string, value: number): string => `
    <div class="tile tile-${cls}">
      <span class="tile-value">${value}</span>
      <span class="tile-label">${esc(label)}</span>
    </div>`;
  return `
  <div class="tiles" role="group" aria-label="Findings by priority">
    ${tile("blocker", "Blockers", blockers)}
    ${tile("should", "Should fix", shouldFix)}
    ${tile("optional", "Nice to have", optional)}
    ${tile("dropped", "Dropped", dropped)}
  </div>`;
}

function renderGateNotes(skeptic: GateSummary | null, judge: GateSummary | null): string {
  const lines: string[] = [];
  const describe = (name: string, s: GateSummary, verb: string): string => {
    const parts: string[] = [];
    if (s.llm > 0) parts.push(`${s.llm} ${verb} by LLM`);
    if (s.deterministic > 0) parts.push(`${s.deterministic} deterministic`);
    if (s.fallback > 0) {
      parts.push(
        `<strong class="warn-text">⚠ ${s.fallback} kept by recall-first fallback${s.timeouts > 0 ? ` (${s.timeouts} timed out)` : ""}</strong>`,
      );
    }
    return `<span class="gate"><span class="gate-name">${esc(name)}</span> ${parts.join(" · ") || "no clusters to assess"}</span>`;
  };
  if (skeptic !== null) lines.push(describe("Skeptic", skeptic, "validated"));
  if (judge !== null) lines.push(describe("Judge", judge, "ranked"));
  if (lines.length === 0) return "";
  return `<div class="gates">${lines.join("")}</div>`;
}

/* ------------------------------- filter bar ------------------------------ */

function renderFilterBar(pool: ReportFinding[]): string {
  const options = (values: string[]): string =>
    [...new Set(values)]
      .sort((a, b) => a.localeCompare(b))
      .map((v) => `<option value="${esc(v)}">${esc(v)}</option>`)
      .join("");

  const severities = options(pool.map((f) => f.cluster.severity));
  const categories = options(pool.map((f) => f.cluster.category));
  const files = options(pool.map((f) => f.cluster.file ?? "(no file)"));
  const supports = options(pool.map((f) => supportKey(f)));

  return `
  <section class="filters" aria-label="Filter findings">
    <input id="f-search" type="search" placeholder="Search title, claim, evidence…" aria-label="Search findings">
    <select id="f-severity" aria-label="Filter by severity"><option value="">severity: all</option>${severities}</select>
    <select id="f-category" aria-label="Filter by category"><option value="">category: all</option>${categories}</select>
    <select id="f-file" aria-label="Filter by file"><option value="">file: all</option>${files}</select>
    <select id="f-support" aria-label="Filter by skeptic support"><option value="">skeptic: all</option>${supports}</select>
    <span id="f-count" class="filter-count" role="status"></span>
  </section>`;
}

/** The skeptic facet value a card is filterable by. */
function supportKey(f: ReportFinding): string {
  if (f.skeptic === null) return "not run";
  if (f.skeptic.failure !== null) return "fallback (unvalidated)";
  if (f.support !== null) return `support: ${f.support}`;
  return "deterministic";
}

/* ------------------------------- sections -------------------------------- */

function renderSection(
  id: string,
  heading: string,
  subtitle: string,
  findings: ReportFinding[],
): string {
  const cards =
    findings.length === 0
      ? `<p class="empty">None.</p>`
      : findings.map(renderCard).join("\n");
  return `
  <section class="findings-section" id="section-${esc(id)}" data-section>
    <h2>${esc(heading)} <span class="count" data-count>${findings.length}</span></h2>
    <p class="section-sub">${esc(subtitle)}</p>
    ${cards}
  </section>`;
}

const CLASSIFICATION_LABEL: Record<Exclude<JudgeClassification, "drop">, string> = {
  blocker: "Blocker",
  should_fix_before_review: "Should fix",
  nice_to_have: "Nice to have",
};

const SUPPORT_LABEL: Record<SupportLevel, string> = {
  strong: "strong support",
  medium: "medium support",
  weak: "weak support",
  unsupported: "unsupported",
};

function renderCard(f: ReportFinding): string {
  const c = f.cluster;
  const fileLabel = c.file === null ? "(no file)" : c.file;
  const lines =
    c.line_start === 0 && c.line_end === 0 ? "" : `:${c.line_start}–${c.line_end}`;

  const badges: string[] = [
    `<span class="badge badge-${f.classification === "blocker" ? "blocker" : f.classification === "should_fix_before_review" ? "should" : "optional"}">${CLASSIFICATION_LABEL[f.classification]}</span>`,
    // Skip the severity badge when it would literally repeat the
    // classification badge ("Blocker blocker").
    ...(c.severity === "blocker" && f.classification === "blocker"
      ? []
      : [`<span class="badge badge-severity">${esc(c.severity)}</span>`]),
    `<span class="badge badge-category">${esc(c.category)}</span>`,
  ];

  return `
  <article class="card" data-card
    data-severity="${esc(c.severity)}"
    data-category="${esc(c.category)}"
    data-file="${esc(fileLabel)}"
    data-support="${esc(supportKey(f))}">
    <div class="card-head">
      <div class="badges">${badges.join(" ")}</div>
      <h3>${esc(c.merged_title)}</h3>
      <p class="card-meta">
        <code class="loc">${esc(fileLabel)}${esc(lines)}</code>
        <span class="sep">·</span> ${c.agreement} ${plural(c.agreement, "reviewer")}, ${c.source_finding_ids.length} ${plural(c.source_finding_ids.length, "finding")} merged
        <span class="sep">·</span> score ${f.score.toFixed(2)}
        <span class="sep">·</span> confidence ${c.confidence.toFixed(2)}
      </p>
    </div>

    <p class="claim"><strong>Why this matters.</strong> ${esc(c.claim)}</p>

    ${renderVerdicts(f)}
    ${renderSuggestion("Suggested fix", c.suggested_fix)}
    ${renderSuggestion("Suggested test", c.suggested_test)}
    ${renderEvidence(c.evidence)}
    ${renderProvenance(f)}
  </article>`;
}

/** Skeptic + judge verdict strip; a fallback-kept finding is flagged loudly. */
function renderVerdicts(f: ReportFinding): string {
  const rows: string[] = [];

  if (f.skeptic !== null) {
    if (f.skeptic.failure !== null) {
      const what = f.skeptic.failure.kind === "timeout" ? "Skeptic timed out" : `Skeptic failed (${f.skeptic.failure.kind})`;
      rows.push(
        `<p class="verdict-row verdict-warn">⚠ <strong>${esc(what)}</strong> — finding kept by recall-first fallback; it was <em>not</em> adversarially validated.</p>`,
      );
    } else if (f.support !== null) {
      rows.push(
        `<p class="verdict-row"><span class="verdict-tag verdict-${esc(f.support)}">skeptic: ${esc(SUPPORT_LABEL[f.support])}</span> ${esc(f.skeptic.decision.reason)}</p>`,
      );
    } else {
      rows.push(
        `<p class="verdict-row"><span class="verdict-tag verdict-det">skeptic: deterministic</span> ${esc(f.skeptic.decision.reason)}</p>`,
      );
    }
  }

  if (f.judge !== null) {
    if (f.judge.failure !== null) {
      rows.push(
        `<p class="verdict-row verdict-warn">⚠ <strong>Judge ${f.judge.failure.kind === "timeout" ? "timed out" : `failed (${esc(f.judge.failure.kind)})`}</strong> — classified deterministically (recall-first fallback).</p>`,
      );
    } else {
      const src = f.judge.source === "llm" ? "judge" : "judge (deterministic)";
      rows.push(
        `<p class="verdict-row"><span class="verdict-tag verdict-judge">${esc(src)}: ${esc(f.judge.decision.classification)}</span> ${esc(f.judge.decision.reason)}</p>`,
      );
    }
  }

  return rows.length > 0 ? `<div class="verdicts">${rows.join("")}</div>` : "";
}

function renderSuggestion(label: string, value: string | null): string {
  if (value === null || value.trim() === "") return "";
  const v = value.trim();
  if (v.includes("\n") || v.length > 220) {
    return `
    <details class="suggestion">
      <summary>${esc(label)}</summary>
      <pre>${esc(v)}</pre>
    </details>`;
  }
  return `<p class="suggestion-inline"><strong>${esc(label)}.</strong> ${esc(v)}</p>`;
}

/** Strongest evidence first (mergeCluster ordering); the tail collapses. */
function renderEvidence(evidence: string[]): string {
  const inline = evidence.slice(0, HTML_INLINE_EVIDENCE);
  const rest = evidence.slice(HTML_INLINE_EVIDENCE);
  const items = (xs: string[]): string => xs.map((e) => `<li>${esc(e)}</li>`).join("");
  const more =
    rest.length === 0
      ? ""
      : `
      <details class="evidence-more">
        <summary>Show ${rest.length} more evidence ${plural(rest.length, "item")}</summary>
        <ul class="evidence">${items(rest)}</ul>
      </details>`;
  return `
    <div class="evidence-block">
      <p class="evidence-title">Evidence <span class="count">${evidence.length}</span></p>
      <ul class="evidence">${items(inline)}</ul>
      ${more}
    </div>`;
}

/** Source agents — the provenance strip at the card's foot. */
function renderProvenance(f: ReportFinding): string {
  const agents = f.cluster.source_agents.map((a) => `<code>${esc(a)}</code>`).join(" ");
  return `<p class="provenance">Reported by ${agents}</p>`;
}

/* ----------------------------- verification ------------------------------ */

function renderVerification(v: PacketVerification): string {
  let body: string;
  if (!v.ran) {
    body = `<p class="empty">Verification not run (detection only). Re-run with <code>--verify</code> to execute install + test/lint/build.</p>`;
  } else {
    const rows: string[] = [];
    if (v.install) rows.push(renderVerificationRow(v.install, "install"));
    for (const cmd of v.commands) rows.push(renderVerificationRow(cmd));
    body = `
    <p class="verification-result ${v.allPassed ? "ok-text" : "warn-text"}">${v.allPassed ? "✓ All commands passed" : "✗ Failures present"}</p>
    <div class="verification-rows">${rows.join("")}</div>`;
  }
  return `
  <section class="findings-section" id="section-verification">
    <h2>Verification</h2>
    ${body}
  </section>`;
}

function renderVerificationRow(c: PacketVerificationCommand, label = ""): string {
  const status = c.passed ? `<span class="ok-text">✓ passed</span>` : c.timedOut ? `<span class="warn-text">✗ timed out</span>` : `<span class="warn-text">✗ failed</span>`;
  const output = (c.stderrPreview || c.stdoutPreview || c.spawnError || "").trim();
  const detail =
    !c.passed && output
      ? `
      <details class="cmd-output">
        <summary>output</summary>
        <pre>${esc(output)}</pre>
      </details>`
      : "";
  return `
  <div class="verification-row">
    <code class="cmd">${label ? `<span class="cmd-label">${esc(label)}</span> ` : ""}${esc(c.command)}</code>
    <span class="cmd-status">${status} <span class="muted">exit ${c.exitCode ?? "—"}</span></span>
    ${detail}
  </div>`;
}

/* ------------------------------- dropped --------------------------------- */

function renderDropped(dropped: ReturnType<typeof collectDropped>): string {
  const body =
    dropped.length === 0
      ? `<p class="empty">Nothing was dropped.</p>`
      : `
    <details class="dropped-details">
      <summary>Show ${dropped.length} dropped ${plural(dropped.length, "finding")} and why</summary>
      <ul class="dropped-list">
        ${dropped
          .map(
            (d) =>
              `<li><strong>${esc(d.title)}</strong> <span class="badge badge-dropped">${esc(d.stage)}</span><br><span class="muted">${esc(d.reason)}</span></li>`,
          )
          .join("")}
      </ul>
    </details>`;
  return `
  <section class="findings-section" id="section-dropped">
    <h2>Dropped findings <span class="count">${dropped.length}</span></h2>
    <p class="section-sub">Excluded by the skeptic or the judge — kept here so the tool can always explain what it dropped and why.</p>
    ${body}
  </section>`;
}

/* ------------------------------- artifacts ------------------------------- */

function renderArtifacts(input: ReportInput): string {
  const { paths, options } = input;
  const from = dirname(paths.reportHtml);
  const targets: string[] = [
    paths.runMetadata,
    paths.context.packetJson,
    paths.normalized.allFindings,
    paths.deduped.clusters,
    paths.deduped.stats,
  ];
  if (options.skepticEnabled) targets.push(paths.skeptic.results);
  if (options.judgeEnabled) targets.push(paths.judge.ranked, paths.finalFindings);
  targets.push(paths.reportMd);
  const items = targets
    .map((t) => {
      const rel = relative(from, t);
      return `<li><a href="${fileHref(rel)}"><code>${esc(rel)}</code></a></li>`;
    })
    .join("");
  return `
  <section class="findings-section" id="section-artifacts">
    <h2>Raw artifacts</h2>
    <p class="section-sub">The machine-readable trail behind this report — full evidence, per-agent raw output, and every gate's verdict.</p>
    <ul class="artifact-list">${items}</ul>
  </section>`;
}

